class I18n {
    constructor() {
        this.currentLanguage = 'en';
        this.fallbackLanguage = 'en';
    }

    async init() {
        try {
            const { value } = await Preferences.get({ key: 'language' });
            if (value && translations[value]) {
                this.currentLanguage = value;
            } else {
                // Auto-detect browser language
                const browserLang = navigator.language.split('-')[0];
                if (translations[browserLang]) {
                    this.currentLanguage = browserLang;
                }
            }
        } catch (err) {
            console.warn('Failed to load language preference:', err);
        }
    }

    async setLanguage(lang) {
        if (translations[lang]) {
            this.currentLanguage = lang;
            try {
                await Preferences.set({ key: 'language', value: lang });
            } catch (err) {
                console.warn('Failed to save language preference:', err);
            }
            return true;
        }
        return false;
    }

    getLanguage() {
        return this.currentLanguage;
    }

    getAvailableLanguages() {
        return Object.keys(translations).map(code => ({
            code,
            name: this.getLanguageName(code)
        }));
    }

    getLanguageName(code) {
        const names = {
            en: 'English',
            es: 'Español',
            fr: 'Français',
            de: 'Deutsch',
            it: 'Italiano',
            ru: 'Русский',
            zh: '中文'
        };
        return names[code] || code;
    }

    t(key, params = {}) {
        // Direct key lookup (flat structure)
        let translation = translations[this.currentLanguage]?.[key];

        // Fallback to English if translation not found
        if (translation === undefined) {
            translation = translations[this.fallbackLanguage]?.[key];
        }

        if (translation === undefined) {
            console.warn(`Translation missing for key: ${key}`);
            return key;
        }

        // Handle pluralization
        if (params.count !== undefined && params.count !== 1) {
            const pluralKey = `${key}_plural`;
            const pluralTranslation = translations[this.currentLanguage]?.[pluralKey]
                || translations[this.fallbackLanguage]?.[pluralKey];
            if (pluralTranslation) {
                translation = pluralTranslation;
            }
        }

        // Replace parameters {{param}}
        return translation.replace(/\{\{(\w+)\}\}/g, (match, param) => {
            return params[param] !== undefined ? params[param] : match;
        });
    }
}

export const i18n = new I18n();
export default i18n;